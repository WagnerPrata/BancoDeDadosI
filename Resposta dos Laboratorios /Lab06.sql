/* Esse Script cria um esquema de dados (banco de dados) para controlar
   os bugs encontrados em módulos de projetos de Informática dentro de uma empresa.
   No esquema temos as seguintes tabelas:
   TB_CARGO 		-  armazena os diversos cargos dos funcionários. 
   TB_FUNCIONARIO 	-  armazena os funcionários da empresa.
   TB_PROJETO           -  Cadastro dos Projetos da Empresa.
   TB_MODULO            -  Cadastro dos Módulos de Cada Projeto. Um Projeto projeto pode
                           ter N Módulos. Um Módulo está associado a apenas um Projeto.
   TB_BUGS              -  Armazena os BUGS que os funcionários (Analistas, Programadores ou Gerentes)
                           encontram ao testar Módulos dos Projetos. Cada BUG é cadastrado por
                           um Funcionário e está ligado a um Módulo de Projeto. Todo Bug possui
                           um Código, uma Descrição, uma Data de Notificação (Data em que foi 
                           cadastrado, e uma Data (DT_Resolvido) que informa a data em que o 
                           BUG foi corrigido. No momento de cadastro do BUG, DT_Resolvido possui
                           o valor NULL. */


   
